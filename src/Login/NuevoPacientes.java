    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        MongoClient mongoClient = new MongoClient("localhost", 27017);
        String civil = cmbCivil.getSelectedItem().toString();
        String sexo = cmbSex.getSelectedItem().toString();
        MongoDatabase database = mongoClient.getDatabase("consultorio");
        MongoCollection<Document> collection = database.getCollection("pacientes");

        Document document = new Document();
        document.append("nombre", txtNombre.getText());
        document.append("edad", txtEdad.getText());
        document.append("estadocivil", civil);
        document.append("sexo", sexo);
        document.append("numtel", txtTel.getText());
        document.append("correo", txtCorreo.getText());
        document.append("contrasena", txtContra.getText());

        collection.insertOne(document);
        mongoClient.close();

        // Mostrar un mensaje al usuario indicando que la cita se ha guardado correctamente
        JOptionPane.showMessageDialog(this, "Se registro correctamente");
        this.dispose();
    }//GEN-LAST:event_btnregistrarActionPerformed

